local set = vim.opt

-- Tabs & spaces
set.expandtab = true -- convert tabs to spaces when pressing <Tab>
set.tabstop = 2 -- size of a hard tabstop (ts)
set.softtabstop = 2 -- number of spaces a <Tab> counts for

-- Indentation
set.shiftwidth = 2 -- size of an indentation (sw)
set.smartindent = true -- automatically insert indents based on the previous line
